package OOPSConceptPart1;

public class FinalConcept {

	public static void main(String[] args) {
		final int i = 10;

		/*
		 * final is a key word. If final keyword is used before a variable, then we
		 * can't change the value of the variable
		 */

		System.out.println(i);

		// i = 20; you can't change the value of variable declared as final

	}

}
