package JavaBasics;

public class DataTypes {

	public static void main(String[] args) {

		// This is my first Java Program.
		// integer, double, character, string are four data types
		int i = 10;
		System.out.println(i);// i value is now 10
		i = 20;// Now i value changed to 20
		System.out.println(i);

		double d = 100;
		System.out.println(d);

		char w = 'a';
		char z = 'A';
		char q = '$';
		char s = '1';
		System.out.println(w);
		System.out.println(z);
		System.out.println(q);
		System.out.println(s);

		String x = "Hi This is my First Java Program. Wish me luck. I want to be a world class SDET";
		System.out.println(x);
	}

}
